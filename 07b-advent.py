import itertools
import queue
from intcode import Program

def run_with_queues(instructions, readQ, writeQ):
  def read():
    while True:
      try:
        x = readQ.get_nowait()
        yield x
        break
      except queue.Empty:
        yield None
  def write(x):
    writeQ.put_nowait(x)
  return Program(instructions, read, write)

def pipelined(instructions, primers):
  qs = [queue.Queue() for _ in range(6)]
  for q, p in zip(qs, primers):
    q.put(p)
  qs[0].put(0) # send first signal
  coroutines = [
    run_with_queues(instructions, qs[ix], qs[(ix + 1) % 5])
    for ix in range(5)
  ]
  for _ in itertools.zip_longest(*coroutines):
    pass
  return qs[0].get_nowait()

